from abc import ABC, abstractmethod

# Базовий інтерфейс Посередника
class Mediator(ABC):
    @abstractmethod
    def notify(self, sender, event: str):
        pass

# Базовий клас для всіх розумних пристроїв (Колег)
class SmartDevice(ABC):
    def __init__(self, mediator: Mediator = None):
        self.mediator = mediator

class MotionSensor(SmartDevice):
    def detect_motion(self):
        print("[Сенсор] Фіксую рух у кімнаті!")
        # Сенсор нічого не вмикає сам, лише сповіщає Посередника
        if self.mediator:
            self.mediator.notify(self, "motion_detected")

class Light(SmartDevice):
    def turn_on(self):
        print("[Світло] Увімкнено яскраве освітлення.")
        
    def turn_off(self):
        print("[Світло] Освітлення вимкнено.")

class Alarm(SmartDevice):
    def trigger(self):
        print("[Сигналізація] УВАГА! Увімкнено гучну сирену!")

class SmartHomeHub(Mediator):
    def __init__(self):
        self.sensor = None
        self.light = None
        self.alarm = None
        self.is_armed = True  # Стан системи: режим охорони увімкнено за замовчуванням

    # Реєстрація пристроїв та прив'язка їх до Хабу
    def register_devices(self, sensor: MotionSensor, light: Light, alarm: Alarm):
        self.sensor = sensor
        self.sensor.mediator = self
        
        self.light = light
        self.light.mediator = self
        
        self.alarm = alarm
        self.alarm.mediator = self

    # Вся складна логіка взаємодії зосереджена ТУТ
    def notify(self, sender, event: str):
        if event == "motion_detected" and isinstance(sender, MotionSensor):
            print("[Хаб-Посередник] Отримав сигнал від сенсора. Аналізую ситуацію...")
            
            # Світло вмикається в будь-якому випадку, щоб було видно, хто зайшов
            self.light.turn_on() 
            
            # Логіка прийняття рішень залежить від стану Хабу
            if self.is_armed:
                print("[Хаб-Посередник] Система під охороною! Викликаю тривогу!")
                self.alarm.trigger()
            else:
                print("[Хаб-Посередник] Охорона вимкнена. Це просто господарі. Тривогу скасовано.\n")

if __name__ == "__main__":
    # Створюємо розумні пристрої (вони повністю незалежні один від одного)
    my_sensor = MotionSensor()
    my_light = Light()
    my_alarm = Alarm()

    # Встановлюємо Хаб та підключаємо до нього наші пристрої
    hub = SmartHomeHub()
    hub.register_devices(my_sensor, my_light, my_alarm)

    # --- Симуляція 1: Господарів немає вдома (Охорона УВІМКНЕНА) ---
    print("--- СИМУЛЯЦІЯ 1: У БУДИНОК ПРОБРАВСЯ ЗЛОДІЙ ---")
    my_sensor.detect_motion()
    print("-" * 50)

    # --- Симуляція 2: Господар повернувся додому ---
    print("--- СИМУЛЯЦІЯ 2: ГОСПОДАР ПОВЕРНУВСЯ ВДОМА ---")
    # Змінюємо стан системи через Хаб (вимикаємо сигналізацію)
    hub.is_armed = False
    
    # Господар проходить повз той самий датчик
    my_sensor.detect_motion()
                